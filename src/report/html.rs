/// An attack scenario generated from findings
#[derive(Serialize, Clone)]
struct AttackScenario {
    title: String,
    description: String,
    impact: String,
    likelihood: String,
    severity_class: String,
}

/// A remediation step generated from findings
#[derive(Serialize, Clone)]
struct RemediationStep {
    priority: String,
    finding: String,
    action: String,
    effort: String,
    severity_class: String,
}

