/// Generates attack scenarios from findings based on CWE patterns
fn generate_attack_scenarios(findings: &[Finding], lang: &str) -> Vec<AttackScenario> {
    let mut scenarios = Vec::new();
    let mut seen = std::collections::HashSet::new();

    let cwes: Vec<String> = findings
        .iter()
        .filter_map(|f| f.cwe_id.as_ref())
        .cloned()
        .collect();

    let is_es = lang == "es";

    if cwes.iter().any(|c| c == "CWE-319") && !seen.contains("mitm") {
        seen.insert("mitm");
        scenarios.push(if is_es {
            AttackScenario {
                title: "Ataque Man-in-the-Middle (Intercepción de Red)".into(),
                description: "Sin HSTS, la primera conexión del navegador puede ser interceptada. Un atacante en la misma red (WiFi, LAN corporativa) puede usar herramientas como sslstrip para degradar HTTPS a HTTP, capturando credenciales y tokens de sesión en texto plano.".into(),
                impact: "Robo completo de credenciales. El atacante obtiene acceso autenticado a la aplicación con los permisos de la víctima. Todos los datos transmitidos durante la sesión quedan expuestos.".into(),
                likelihood: "Medio — Requiere proximidad de red (mismo WiFi, ARP spoofing). Común en redes compartidas.".into(),
                severity_class: "High".into(),
            }
        } else {
            AttackScenario {
                title: "Man-in-the-Middle Attack (Network Interception)".into(),
                description: "Without HSTS, the first connection from a user's browser can be intercepted. An attacker on the same network (WiFi, corporate LAN) can use tools like sslstrip to downgrade HTTPS to HTTP, capturing credentials and session tokens in plaintext.".into(),
                impact: "Complete credential theft. Attacker gains authenticated access to the application with the victim's permissions. All data transmitted during the session is exposed.".into(),
                likelihood: "Medium — Requires network proximity (same WiFi, ARP spoofing). Common in shared networks.".into(),
                severity_class: "High".into(),
            }
        });
    }

    if cwes.iter().any(|c| c == "CWE-1021") && !seen.contains("clickjacking") {
        seen.insert("clickjacking");
        scenarios.push(if is_es {
            AttackScenario {
                title: "Clickjacking (Ataque de Redirección de UI)".into(),
                description: "La aplicación puede ser embebida en un iframe invisible en una página controlada por el atacante. Los usuarios interactúan con lo que creen es un sitio legítimo, pero sus clics se ejecutan en la aplicación embebida — enviando formularios, cambiando configuraciones o autorizando acciones.".into(),
                impact: "Acciones no autorizadas ejecutadas bajo la sesión de la víctima. Recolección de credenciales mediante formularios de login superpuestos.".into(),
                likelihood: "Medio — Requiere ingeniería social para atraer víctimas a la página del atacante.".into(),
                severity_class: "Medium".into(),
            }
        } else {
            AttackScenario {
                title: "Clickjacking (UI Redress Attack)".into(),
                description: "The application can be embedded in an invisible iframe on an attacker-controlled page. Users interact with what they think is a legitimate site, but their clicks are actually performed on the embedded application — submitting forms, changing settings, or authorizing actions.".into(),
                impact: "Unauthorized actions performed under the victim's session. Credential harvesting through overlaid login forms.".into(),
                likelihood: "Medium — Requires social engineering to lure victims to the attacker's page.".into(),
                severity_class: "Medium".into(),
            }
        });
    }

    if cwes.iter().any(|c| c == "CWE-693") && !seen.contains("xss-csp") {
        seen.insert("xss-csp");
        scenarios.push(if is_es {
            AttackScenario {
                title: "Amplificación de XSS (Sin CSP)".into(),
                description: "Sin Content-Security-Policy, cualquier vulnerabilidad XSS se vuelve significativamente más peligrosa. Scripts inyectados pueden cargar recursos externos, exfiltrar datos a servidores del atacante, registrar teclas y secuestrar sesiones sin restricción del navegador.".into(),
                impact: "Si se encuentra un vector XSS (almacenado o reflejado), el atacante tiene ejecución de scripts sin restricciones. La exfiltración de datos, secuestro de sesión y toma de control de cuentas se vuelven triviales.".into(),
                likelihood: "Bajo-Medio — Requiere un vector XSS, pero la falta de CSP elimina toda mitigación.".into(),
                severity_class: "Medium".into(),
            }
        } else {
            AttackScenario {
                title: "Cross-Site Scripting Amplification (No CSP)".into(),
                description: "Without Content-Security-Policy, any XSS vulnerability becomes significantly more dangerous. Injected scripts can load external resources, exfiltrate data to attacker-controlled servers, keylog input fields, and hijack sessions without any browser restriction.".into(),
                impact: "If an XSS vector is found (stored or reflected), the attacker has unrestricted script execution. Data exfiltration, session hijacking, and full account takeover become trivial.".into(),
                likelihood: "Low-Medium — Requires an XSS vector, but the lack of CSP removes all mitigation.".into(),
                severity_class: "Medium".into(),
            }
        });
    }

    if cwes.iter().any(|c| c == "CWE-352") && !seen.contains("csrf") {
        seen.insert("csrf");
        scenarios.push(if is_es {
            AttackScenario {
                title: "Cross-Site Request Forgery (Acciones No Autorizadas)".into(),
                description: "Formularios sin protección CSRF permiten a un atacante crear una página maliciosa que envía solicitudes automáticamente en nombre de un usuario autenticado. Visitar la página del atacante mientras está logueado ejecuta acciones que el usuario nunca pretendió.".into(),
                impact: "Cambios de estado no autorizados: restablecimiento de contraseñas, modificaciones de datos, escalamiento de privilegios. Login CSRF puede forzar autenticación con credenciales controladas por el atacante.".into(),
                likelihood: "Medio — Requiere que la víctima visite la página del atacante mientras está autenticada.".into(),
                severity_class: "Medium".into(),
            }
        } else {
            AttackScenario {
                title: "Cross-Site Request Forgery (Unauthorized Actions)".into(),
                description: "Forms without CSRF protection allow an attacker to craft a malicious page that automatically submits requests on behalf of an authenticated user. Visiting the attacker's page while logged in triggers actions the user never intended.".into(),
                impact: "Unauthorized state changes: password resets, data modifications, privilege escalation. Login CSRF can force authentication with attacker-controlled credentials for session monitoring.".into(),
                likelihood: "Medium — Requires victim to visit attacker's page while authenticated.".into(),
                severity_class: "Medium".into(),
            }
        });
    }

    if cwes.iter().any(|c| c == "CWE-89") && !seen.contains("sqli") {
        seen.insert("sqli");
        scenarios.push(if is_es {
            AttackScenario {
                title: "Compromiso de Base de Datos via Inyección SQL".into(),
                description: "La inyección SQL permite a un atacante ejecutar consultas arbitrarias en la base de datos. Esto puede evadir autenticación, extraer contenidos completos de la base de datos, modificar o eliminar registros, y en algunos casos ejecutar comandos del sistema operativo en el servidor de base de datos.".into(),
                impact: "Compromiso completo de la base de datos. Exfiltración masiva de datos (registros de usuarios, credenciales, datos de negocio). Manipulación o destrucción de datos. Potencial movimiento lateral a sistemas internos.".into(),
                likelihood: "Alto — Herramientas automatizadas pueden explotar inyección SQL con mínimo esfuerzo.".into(),
                severity_class: "Critical".into(),
            }
        } else {
            AttackScenario {
                title: "Database Compromise via SQL Injection".into(),
                description: "SQL injection allows an attacker to execute arbitrary database queries. This can bypass authentication, extract entire database contents, modify or delete records, and in some cases execute operating system commands on the database server.".into(),
                impact: "Complete database compromise. Mass data exfiltration (user records, credentials, business data). Data manipulation or destruction. Potential lateral movement to internal systems.".into(),
                likelihood: "High — Automated tools can exploit SQL injection with minimal effort.".into(),
                severity_class: "Critical".into(),
            }
        });
    }

    if cwes.iter().any(|c| c == "CWE-79") && !seen.contains("xss") {
        seen.insert("xss");
        scenarios.push(if is_es {
            AttackScenario {
                title: "Toma de Control de Cuenta via Cross-Site Scripting".into(),
                description: "XSS reflejado o almacenado permite a un atacante ejecutar JavaScript en el contexto de las sesiones de otros usuarios. Esto habilita robo de cookies de sesión, registro de teclas de credenciales, desfiguración y redirección a páginas de phishing.".into(),
                impact: "Secuestro de sesión y toma de control de cuentas. XSS almacenado afecta a cada usuario que visualiza la página infectada. Robo de credenciales mediante formularios de login falsos inyectados en la página.".into(),
                likelihood: "Alto — XSS reflejado requiere un enlace manipulado; XSS almacenado afecta a todos los visitantes automáticamente.".into(),
                severity_class: "High".into(),
            }
        } else {
            AttackScenario {
                title: "Account Takeover via Cross-Site Scripting".into(),
                description: "Reflected or stored XSS allows an attacker to execute JavaScript in the context of other users' sessions. This enables session cookie theft, keylogging of credentials, defacement, and redirection to phishing pages.".into(),
                impact: "Session hijacking and account takeover. Stored XSS affects every user who views the infected page. Credential theft through fake login forms injected into the page.".into(),
                likelihood: "High — Reflected XSS requires a crafted link; Stored XSS affects all visitors automatically.".into(),
                severity_class: "High".into(),
            }
        });
    }

    if cwes.iter().any(|c| c == "CWE-1336") && !seen.contains("ssti") {
        seen.insert("ssti");
        scenarios.push(if is_es {
            AttackScenario {
                title: "Ejecución Remota de Código via Inyección de Plantillas".into(),
                description: "Server-Side Template Injection permite a un atacante inyectar expresiones de plantilla que se evalúan en el servidor. Dependiendo del motor de plantillas, esto puede escalar desde divulgación de información hasta ejecución remota de código completa en el servidor.".into(),
                impact: "Ejecución remota de código en el servidor. Compromiso total del sistema. Acceso a bases de datos internas, sistemas de archivos y recursos de red. Exfiltración de datos a escala.".into(),
                likelihood: "Alto — Una vez identificado, la explotación de SSTI está bien documentada para todos los motores de plantillas principales.".into(),
                severity_class: "Critical".into(),
            }
        } else {
            AttackScenario {
                title: "Remote Code Execution via Template Injection".into(),
                description: "Server-Side Template Injection allows an attacker to inject template expressions that are evaluated on the server. Depending on the template engine, this can escalate from information disclosure to full remote code execution on the server.".into(),
                impact: "Remote code execution on the server. Full system compromise. Access to internal databases, file systems, and network resources. Data exfiltration at scale.".into(),
                likelihood: "High — Once identified, SSTI exploitation is well-documented for all major template engines.".into(),
                severity_class: "Critical".into(),
            }
        });
    }

    if cwes.iter().any(|c| c == "CWE-78") && !seen.contains("cmdi") {
        seen.insert("cmdi");
        scenarios.push(if is_es {
            AttackScenario {
                title: "Compromiso del Servidor via Inyección de Comandos OS".into(),
                description: "La inyección de comandos permite a un atacante ejecutar comandos arbitrarios del sistema operativo en el servidor. Esto proporciona acceso directo al sistema subyacente, evadiendo todos los controles a nivel de aplicación.".into(),
                impact: "Compromiso completo del servidor. Acceso al sistema de archivos, recolección de credenciales, movimiento lateral a otros sistemas, instalación de backdoors o malware.".into(),
                likelihood: "Alto — La explotación es directa una vez identificado el punto de inyección.".into(),
                severity_class: "Critical".into(),
            }
        } else {
            AttackScenario {
                title: "Server Compromise via OS Command Injection".into(),
                description: "Command injection allows an attacker to execute arbitrary operating system commands on the server. This provides direct access to the underlying system, bypassing all application-level controls.".into(),
                impact: "Complete server compromise. File system access, credential harvesting, lateral movement to other systems, installation of backdoors or malware.".into(),
                likelihood: "High — Exploitation is straightforward once the injection point is identified.".into(),
                severity_class: "Critical".into(),
            }
        });
    }

    if cwes.iter().any(|c| c == "CWE-200") && !seen.contains("info-disc") {
        seen.insert("info-disc");
        scenarios.push(if is_es {
            AttackScenario {
                title: "Explotación Dirigida via Divulgación de Información".into(),
                description: "Los headers de versión del servidor y las huellas tecnológicas permiten a los atacantes identificar versiones exactas del software ejecutándose en el servidor. Esto habilita búsquedas dirigidas de CVEs conocidos y exploits públicos específicos para las versiones detectadas.".into(),
                impact: "Reduce significativamente el esfuerzo del atacante. En lugar de pruebas a ciegas, el atacante puede usar exploits conocidos para la versión específica detectada.".into(),
                likelihood: "Alto — Scanners automatizados recopilan esta información rutinariamente. Este es frecuentemente el primer paso en un ataque dirigido.".into(),
                severity_class: "Low".into(),
            }
        } else {
            AttackScenario {
                title: "Targeted Exploitation via Information Disclosure".into(),
                description: "Server version headers and technology fingerprints allow attackers to identify exact software versions running on the server. This enables targeted searches for known CVEs and public exploits specific to the detected versions.".into(),
                impact: "Reduces attacker effort significantly. Instead of blind testing, the attacker can use known exploits for the specific version detected.".into(),
                likelihood: "High — Automated scanners routinely collect this information. This is often the first step in a targeted attack.".into(),
                severity_class: "Low".into(),
            }
        });
    }

    if cwes.iter().any(|c| c == "CWE-319") && cwes.iter().any(|c| c == "CWE-352") && !seen.contains("chain-session") {
        seen.insert("chain-session");
        scenarios.push(if is_es {
            AttackScenario {
                title: "Cadena de Ataque: Intercepción de Red + CSRF".into(),
                description: "La combinación de HSTS faltante y vulnerabilidades CSRF crea una cadena de ataque. Un atacante en la red intercepta la conexión HTTP inicial (sin HSTS), inyecta un payload CSRF en la respuesta, y el navegador de la víctima ejecuta acciones no autorizadas con su sesión autenticada.".into(),
                impact: "Impacto combinado: robo de credenciales Y acciones no autorizadas. El atacante tanto roba la sesión como la usa para realizar acciones que la víctima nunca pretendió.".into(),
                likelihood: "Medio — Requiere proximidad de red pero la cadena está bien establecida.".into(),
                severity_class: "High".into(),
            }
        } else {
            AttackScenario {
                title: "Attack Chain: Network Interception + CSRF".into(),
                description: "The combination of missing HSTS and CSRF vulnerabilities creates an attack chain. An attacker on the network intercepts the initial HTTP connection (no HSTS), injects a CSRF payload into the response, and the victim's browser executes unauthorized actions with their authenticated session.".into(),
                impact: "Combined impact: credential theft AND unauthorized actions. The attacker both steals the session and uses it to perform actions the victim never intended.".into(),
                likelihood: "Medium — Requires network proximity but the chain is well-established.".into(),
                severity_class: "High".into(),
            }
        });
    }

    scenarios.sort_by(|a, b| {
        let ord = |s: &str| match s { "Critical" => 0, "High" => 1, "Medium" => 2, _ => 3 };
        ord(&a.severity_class).cmp(&ord(&b.severity_class))
    });

    scenarios
}

/// Generates a prioritized remediation plan from findings
fn generate_remediation_plan(findings: &[Finding], lang: &str) -> Vec<RemediationStep> {
    let mut steps = Vec::new();
    let mut seen_cwes = std::collections::HashSet::new();
    let is_es = lang == "es";

    let mut sorted: Vec<&Finding> = findings.iter().collect();
    sorted.sort_by(|a, b| a.severity.cmp(&b.severity));

    for f in &sorted {
        let cwe = f.cwe_id.as_deref().unwrap_or("");
        if cwe.is_empty() || seen_cwes.contains(cwe) {
            continue;
        }
        seen_cwes.insert(cwe.to_string());

        let (priority, action, effort) = if is_es {
            match cwe {
                "CWE-89" => ("P0 — Bloqueante", "Usar consultas parametrizadas/prepared statements. Revisar todas las consultas a base de datos por concatenación de strings con entrada del usuario.", "Medio"),
                "CWE-79" => ("P0 — Bloqueante", "Implementar codificación de salida según contexto. Desplegar header Content-Security-Policy para restringir ejecución de scripts.", "Medio"),
                "CWE-1336" => ("P0 — Bloqueante", "Nunca pasar entrada del usuario a motores de plantillas. Usar plantillas sandboxed/sin lógica. Validar y sanitizar todo contenido dinámico.", "Medio"),
                "CWE-78" => ("P0 — Bloqueante", "Eliminar construcción de comandos OS desde entrada del usuario. Usar APIs nativas del lenguaje en lugar de comandos de shell.", "Medio"),
                "CWE-319" => ("P1 — Pre-lanzamiento", "Agregar header Strict-Transport-Security: max-age=31536000; includeSubDomains. Configurar redirección HTTPS a nivel de load balancer/proxy.", "Bajo"),
                "CWE-1021" => ("P1 — Pre-lanzamiento", "Agregar header X-Frame-Options: DENY. También agregar frame-ancestors 'none' en CSP como defensa en profundidad.", "Bajo"),
                "CWE-352" => ("P1 — Pre-lanzamiento", "Implementar tokens anti-CSRF en todos los formularios que cambien estado. Usar SameSite=Strict o Lax en cookies de sesión.", "Medio"),
                "CWE-693" => ("P2 — Próximo sprint", "Desplegar headers Content-Security-Policy, X-Content-Type-Options, Referrer-Policy y Permissions-Policy.", "Bajo"),
                "CWE-200" => ("P2 — Próximo sprint", "Remover u ofuscar header Server. Deshabilitar divulgación de versión en la configuración del servidor de aplicación.", "Bajo"),
                "CWE-93" => ("P1 — Pre-lanzamiento", "Eliminar caracteres CRLF (\\r\\n) de toda entrada de usuario reflejada en headers HTTP.", "Bajo"),
                _ => {
                    let action = if f.recommendation.is_empty() { "Revisar y remediar según mejores prácticas de seguridad." } else { &f.recommendation };
                    ("P3 — Backlog", action, "Variable")
                }
            }
        } else {
            match cwe {
                "CWE-89" => ("P0 — Blocker", "Use parameterized queries/prepared statements. Review all database queries for string concatenation with user input.", "Medium"),
                "CWE-79" => ("P0 — Blocker", "Implement context-aware output encoding. Deploy Content-Security-Policy header to restrict script execution.", "Medium"),
                "CWE-1336" => ("P0 — Blocker", "Never pass user input to template engines. Use sandboxed/logic-less templates. Validate and sanitize all dynamic content.", "Medium"),
                "CWE-78" => ("P0 — Blocker", "Eliminate OS command construction from user input. Use language-native APIs instead of shell commands.", "Medium"),
                "CWE-319" => ("P1 — Pre-launch", "Add Strict-Transport-Security header: max-age=31536000; includeSubDomains. Configure HTTPS redirect at load balancer/proxy level.", "Low"),
                "CWE-1021" => ("P1 — Pre-launch", "Add X-Frame-Options: DENY header. Also add frame-ancestors 'none' in CSP for defense in depth.", "Low"),
                "CWE-352" => ("P1 — Pre-launch", "Implement anti-CSRF tokens in all state-changing forms. Use SameSite=Strict or Lax on session cookies.", "Medium"),
                "CWE-693" => ("P2 — Next sprint", "Deploy Content-Security-Policy, X-Content-Type-Options, Referrer-Policy, and Permissions-Policy headers.", "Low"),
                "CWE-200" => ("P2 — Next sprint", "Remove or obfuscate Server header. Disable version disclosure in application server configuration.", "Low"),
                "CWE-93" => ("P1 — Pre-launch", "Strip CRLF characters (\\r\\n) from all user input reflected in HTTP headers.", "Low"),
                _ => {
                    let action = if f.recommendation.is_empty() { "Review and remediate according to security best practices." } else { &f.recommendation };
                    ("P3 — Backlog", action, "Varies")
                }
            }
        };

        let sev_class = format!("{:?}", f.severity);

        steps.push(RemediationStep {
            priority: priority.into(),
            finding: f.title.clone(),
            action: action.into(),
            effort: effort.into(),
            severity_class: sev_class,
        });
    }

    steps
}

